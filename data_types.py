# Python Data Types Example

## String Operations
my_string = "Hello, World!"
print(my_string)

# String Concatenation
new_string = my_string + " Welcome!"
print(new_string)

# Accessing Characters
print(my_string[0])  # Output: H

## Number Operations
num1 = 10
num2 = 5

# Addition
print(num1 + num2)  # Output: 15

# Subtraction
print(num1 - num2)  # Output: 5

## List Operations
my_list = [1, 2, 3, 4, 5]

# Append
my_list.append(6)
print(my_list)

# Accessing Elements
print(my_list[0])  # Output: 1

## Dictionary Operations
dict_example = {"name": "Alice", "age": 25}

# Accessing Values
print(dict_example["name"])  # Output: Alice

# Adding New Key-Value Pair
dict_example["city"] = "New York"
print(dict_example)

## Tuple Operations
my_tuple = (1, 2, 3)

# Accessing Elements
print(my_tuple[1])  # Output: 2

## Set Operations
my_set = {1, 2, 3}

# Adding Elements
my_set.add(4)
print(my_set)  # Output: {1, 2, 3, 4}
